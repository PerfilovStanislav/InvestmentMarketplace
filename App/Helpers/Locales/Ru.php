    public function getPeriodName(int $i, int $k): string {
        return ['минут', 'час', '', 'недел', 'месяц', ''][$i-1].(
            ($k+89)%100<4 || ($k+9)%10>3
                ?['', 'ов', 'дней', 'ь', 'ев', 'лет'][$i-1]
                :[['а', 'ы'], ['', 'а'], ['день', 'дня'], ['я', 'и'], ['', 'а'], ['год', 'года']][$i-1][(int)($k%10>1)]
            );
    }
